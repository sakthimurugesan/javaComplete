import java.util.Scanner;

/**
 * a_015_ifElse
 */
public class a_015_ifElse {
    public static void main(String[] args) {
        int a=10,b=12;
        if(a>b)
        {
            // true block
            System.out.println("A=10 is greater than B=12");
        }
        else
        {   // false block
            System.out.println("A=10 is lesser than B=12");
        }
    }
    
}
/**
if else statement works based on the given condition
if the condition is true then it will execute the true block
if false it will execute false block
*/